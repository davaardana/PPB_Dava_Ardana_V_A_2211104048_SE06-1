void main() {
  // Membuat fixed-length list dengan panjang 3
  List<int> fixedList = List.filled(3, 0); // List dengan 3 elemen,
// diisi dengan 0

  // Mengubah elemen dalam list
  fixedList[0] = 10;
  fixedList[1] = 20;
  fixedList[2] = 30;
  print(fixedList); // Output: [10, 20, 30]

  // fixedList.add(40); //
}
